package com.jojones.teamone_paint;

import android.graphics.Paint;

class Circle {
    public float x;
    public float y;
    public int r;
    public Paint p;

    public Circle(float x, float y, int r, Paint p) {
        this.x = x;
        this.y = y;
        this.r = r;
        this.p = p;
    }
}
